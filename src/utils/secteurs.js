/**
 * Obtenir tous les secteurs d'une catégorie
 */
export function getSecteursByCategory(category) {
  return SECTEURS_BY_CATEGORY[category] || []
}

/**
 * Obtenir la catégorie d'un secteur
 */
export function getCategoryBySecteur(secteur) {
  for (const [category, secteurs] of Object.entries(SECTEURS_BY_CATEGORY)) {
    if (secteurs.includes(secteur)) {
      return category
    }
  }
  return 'Autres'
}

/**
 * Rechercher des secteurs par mot-clé
 */
export function searchSecteurs(query) {
  const queryLower = query.toLowerCase()
  return SECTEURS_ACTIVITES.filter(secteur =>
    secteur.toLowerCase().includes(queryLower)
  )
}




